package com.e.mymostpopulerapi.model;

public class MediaMetaData {

    String format;
    int height;
    int width;
    String url;

    public MediaMetaData() {
    }

    public String getFormat() {
        return format;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }

    public String getUrl() {
        return url;
    }
}
